    private void btnCapNhatDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatDonActionPerformed
        // TODO add your handling code here:
        fillTable = true;
        if (row != -1) {
            int maHD = (Integer) tblHoaDon.getValueAt(row, 0);
            HoaDon hd = hdDAO.selectByID(String.valueOf(maHD));
            if (hd.isTrangThai()) {
                MsgBox.alert(this, "Hóa đơn đã thanh toán!");
            } else {
                HoaDonJDialog dshd = new HoaDonJDialog((Frame) SwingUtilities.getWindowAncestor(this), true);
                dshd.setEditData(hd);
                dshd.setVisible(true);
                fillTableHoaDon();
            }
            fillTable = false;
            row = -1;
        } else {
            MsgBox.alert(this, "Chưa chọn hóa đơn!");
        }
    }//GEN-LAST:event_btnCapNhatDonActionPerformed

