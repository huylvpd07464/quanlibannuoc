    void fillTableHoaDon() {
        DefaultTableModel model = (DefaultTableModel) tblHoaDon.getModel();
        model.setRowCount(0);
        
        String tenKH = txtTenKH.getText();
        List<Object[]> list = hdDAO.getHoaDon(tenKH);
        for (Object[] row : list) {
            boolean trangThai = (boolean) row[4];
            model.addRow(new Object[]{row[0], row[1], row[2], row[3], trangThai ? "Đã thanh toán" : "Chưa thanh toán"});
        }
    }

